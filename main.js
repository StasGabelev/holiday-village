let lang = 'pl';

function applyLang(l) {
  lang = l;
  const t = T[l];

  // text nodes (innerHTML to support <em> and <br>)
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const k = el.getAttribute('data-i18n');
    if (t[k] !== undefined) el.innerHTML = t[k];
  });

  // placeholders
  document.querySelectorAll('[data-i18n-ph]').forEach(el => {
    const k = el.getAttribute('data-i18n-ph');
    if (t[k]) el.placeholder = t[k];
  });

  // <option> elements inside selects
  document.querySelectorAll('select option[data-i18n]').forEach(opt => {
    const k = opt.getAttribute('data-i18n');
    if (t[k]) opt.textContent = t[k];
  });

  // lang buttons active state
  document.querySelectorAll('.lbtn').forEach(b =>
    b.classList.toggle('active', b.dataset.lang === l)
  );

  // html lang attr
  document.documentElement.lang = { pl:'pl', ua:'uk', en:'en', ru:'ru' }[l] || l;
}

document.addEventListener('DOMContentLoaded', () => {
  applyLang('pl');

  // language switcher
  document.querySelectorAll('.lbtn').forEach(b =>
    b.addEventListener('click', () => applyLang(b.dataset.lang))
  );

  // header scroll style
  const hdr = document.getElementById('hdr');
  const onScroll = () => hdr.classList.toggle('scrolled', window.scrollY > 70);
  window.addEventListener('scroll', onScroll, { passive: true });
  onScroll();

  // hamburger
  const burger  = document.getElementById('burger');
  const mobNav  = document.getElementById('mob-nav');
  burger.addEventListener('click', () => {
    burger.classList.toggle('open');
    mobNav.classList.toggle('open');
  });
  mobNav.querySelectorAll('a').forEach(a =>
    a.addEventListener('click', () => {
      burger.classList.remove('open');
      mobNav.classList.remove('open');
    })
  );

  // fade-in on scroll
  const io = new IntersectionObserver(entries =>
    entries.forEach(e => { if (e.isIntersecting) { e.target.classList.add('visible'); io.unobserve(e.target); } }),
    { threshold: 0.07 }
  );
  document.querySelectorAll('.fade-in').forEach(el => io.observe(el));

  // lead form
  const form    = document.getElementById('lead-form');
  const modal   = document.getElementById('success-modal');
  const modalClose = document.getElementById('modal-close-btn');

  function openModal() {
    modal.classList.add('active');
    document.body.style.overflow = 'hidden';
  }
  function closeModal() {
    modal.classList.remove('active');
    document.body.style.overflow = '';
  }
  if (modalClose) modalClose.addEventListener('click', closeModal);
  if (modal) modal.addEventListener('click', e => { if (e.target === modal) closeModal(); });
  document.addEventListener('keydown', e => { if (e.key === 'Escape') closeModal(); });

  if (form) {
    form.addEventListener('submit', async e => {
      e.preventDefault();
      const fd  = new FormData(form);
      const btn = form.querySelector('[type=submit]');
      btn.disabled = true;

      const entry = {
        name:     fd.get('name'),
        phone:    fd.get('phone'),
        date:     fd.get('date'),
        interest: fd.get('interest'),
        lang,
      };

      try {
        await fetch('send.php', {
          method:  'POST',
          headers: { 'Content-Type': 'application/json' },
          body:    JSON.stringify(entry),
        });
      } catch (_) {}

      btn.disabled = false;
      form.reset();
      openModal();
    });
  }
});
